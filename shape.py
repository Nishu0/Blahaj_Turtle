from turtle import *
import turtle as tur

tur.speed(0)
tur.width(2)
color="#ffd202"
def relative_pos(x,y):
	tur.penup()
	tur.goto(tur.pos()+(x,y))
	tur.pendown()

def fillCircle(col,fcol,r):
	tur.begin_fill()
	tur.color(col)
	tur.fillcolor(fcol)
	tur.circle(r)
	tur.end_fill

def drawBlahaj():
	tur.seth(-90)
	tur.circle(80,180)
	tur.begin_fill()
	tur.circle(64,70)
	tur.forward(80/3)
	tur.right(45)
	tur.forward(80/3)
	tur.left(135)
	tur.forward(80/3)
	tur.right(55)
